/**
 * 根據盤面類型和派別獲取對應的User Prompt指導詞
 */
export function getUserPromptInstructions(
  chartType: ChartType,
  language: Language,
): string {
  const isTraditional = language === 'zh-TW'
  
  // 根據盤面類型選擇對應的提示詞
  // chartType 直接對應盤面選擇：四化（northern-flying）、三合（triple-harmony）、飛星（flying-star）
  switch (chartType) {
    case 'northern-flying':
      return isTraditional ? USER_PROMPT_NORTHERN_FLYING_ZH_TW : USER_PROMPT_NORTHERN_FLYING_ZH_CN
    case 'triple-harmony':
      return isTraditional ? USER_PROMPT_TRIPLE_HARMONY_ZH_TW : USER_PROMPT_TRIPLE_HARMONY_ZH_CN
    case 'flying-star':
      return isTraditional ? USER_PROMPT_FLYING_STAR_ZH_TW : USER_PROMPT_FLYING_STAR_ZH_CN
    default:
      // 默認為北派四化
      return isTraditional ? USER_PROMPT_NORTHERN_FLYING_ZH_TW : USER_PROMPT_NORTHERN_FLYING_ZH_CN
  }
}

/**
 * 用户提示词构建
 * 
 * @param language - 語言選擇
 * @param indicatorsJson - 命盤指標JSON
 * @param contextStr - 補充上下文
 * @param chartType - 盤面類型選擇：'northern-flying'(四化)、'triple-harmony'(三合)、'flying-star'(飛星)
 */
export interface UserPromptParams {
  language: Language
  indicatorsJson: string
  contextStr: string
  chartType?: ChartType  // 盤面類型選擇，默認為 'northern-flying'
}

export function buildUserPrompt(params: UserPromptParams): string {
  const {
    language,
    indicatorsJson,
    contextStr,
    chartType = 'northern-flying',
  } = params

  const prefix = t('ai.prompt.readChart', language)
  const jsonLabel = t('ai.prompt.keyIndicators', language)
  const jsonHint = t('ai.prompt.indicatorsHint', language)
  const contextLabel = t('ai.prompt.supplementContext', language)
  const instructions = getUserPromptInstructions(chartType, language)

  // 是否包含補充上下文和大限信息
  const includeContext = false

  return `${prefix}

## ${jsonLabel}
${jsonHint}
${indicatorsJson}

${instructions}
${includeContext ? `\n## ${contextLabel}\n${contextStr}` : ''}`
}

/**
 * 構建完整提示詞（System Prompt + User Prompt）
 */
export function buildFullPrompt(params: UserPromptParams): {
  systemPrompt: string
  userPrompt: string
} {
  return {
    systemPrompt: getSystemPrompt(params.language, params.chartType),
    userPrompt: buildUserPrompt(params),
  }
}