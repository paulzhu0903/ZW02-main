/**
 * 获取系统提示词 - 支持按盤面類型選擇
 */
export function getSystemPrompt(language: Language, chartType: ChartType = 'northern-flying'): string {
  const isTraditional = language === 'zh-TW'
  let basePrompt: string

  switch (chartType) {
    case 'triple-harmony':
      basePrompt = isTraditional ? SYSTEM_PROMPT_TRIPLE_HARMONY_ZH_TW : SYSTEM_PROMPT_TRIPLE_HARMONY_ZH_CN
      break
    case 'flying-star':
      basePrompt = isTraditional ? SYSTEM_PROMPT_FLYING_STAR_ZH_TW : SYSTEM_PROMPT_FLYING_STAR_ZH_CN
      break
    case 'northern-flying':
    default:
      basePrompt = isTraditional ? SYSTEM_PROMPT_NORTHERN_FLYING_ZH_TW : SYSTEM_PROMPT_NORTHERN_FLYING_ZH_CN
      break
  }

  const langHint = isTraditional ? '請使用繁體中文輸出全部內容。' : '请使用简体中文输出全部内容。'
  return `${basePrompt}\n\n${langHint}`
}

