/* 派別與盤型定義 */
export type ChartType = 'northern-flying' | 'triple-harmony' | 'flying-star'  // 盤面類型：四化、三合、飛星
export type UIChartType = 'flying' | 'trireme' | 'transformation'  // UI 層的盤面類型

/**
 * 將 UI 層的 chartType 轉換為 prompt 系統的 ChartType
 */
export function mapUIChartTypeToPromptChartType(uiChartType: UIChartType): ChartType {
  const mapping: Record<UIChartType, ChartType> = {
    'flying': 'flying-star',      // 飛星 → flying-star
    'trireme': 'triple-harmony',   // 三合 → triple-harmony
    'transformation': 'northern-flying'  // 四化 → northern-flying
  }
  return mapping[uiChartType]
}

